     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store()
    {
        $this->validate(request(),[

            'title'=>'required',
            'body'=>'required'
        ]);

        Post::create([

            'title'=>request('title'),
            'body' => request('body'),
            'user_id' => auth()->id()
        ]);

        return redirect('/posts');
    }

